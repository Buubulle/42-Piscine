int	ft_iterative_factorial(int nb)
{
	int	i;

	i = 1;
	while (nb < 0)
		return (0);
	while ((nb == 1) || (nb == 0))
		return (1);
	while (nb > 1)
		i *= nb--;
	return (i);
}
