int	ft_str_is_lowercase(char *str)
{
	int	i;
	int	nb;

	i = 0;
	while (str[i] != '\0')
	{
		if ((str[i] >= 'a' && str[i] <= 'z'))
		{
			nb = 1;
		}
		else
		{
			return (0);
		}
		i++;
	}
	nb = 1;
	return (nb);
}
